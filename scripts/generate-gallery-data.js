// @ts-nocheck
const fs = require('fs');
const path = require('path');

const MUSTERI_DIR = path.join(__dirname, '../public/media/galeri/musteri');
const OUTPUT_FILE = path.join(__dirname, '../src/lib/gallery-media.ts');

if (!fs.existsSync(MUSTERI_DIR)) {
  console.error(`Directory not found: ${MUSTERI_DIR}`);
  process.exit(1);
}

const files = fs.readdirSync(MUSTERI_DIR);
const items = [];

files.forEach(file => {
  const ext = path.extname(file).toLowerCase();
  const filePath = `/media/galeri/musteri/${file}`;
  
  if (['.jpg', '.jpeg', '.png', '.webp'].includes(ext)) {
    items.push({
      src: filePath,
      type: 'photo'
    });
  } else if (['.mov', '.mp4'].includes(ext)) {
    items.push({
      src: filePath,
      type: 'video'
    });
  }
});

// Sort items alphabetically to ensure consistency
items.sort((a, b) => a.src.localeCompare(b.src));

const outputContent = `// This file is auto-generated by scripts/generate-gallery-data.js
// Do not edit manually.

export interface GalleryItem {
  src: string;
  type: "photo" | "video";
}

export const GALLERY_ITEMS: GalleryItem[] = ${JSON.stringify(items, null, 2)};
`;

fs.writeFileSync(OUTPUT_FILE, outputContent, 'utf-8');
console.log(`Generated ${items.length} gallery items into ${OUTPUT_FILE}`);
